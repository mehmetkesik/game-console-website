<p class="tm-color-primary">What is Grand Theft Auto?</p>

<p>
    Grand Theft Auto, officially abbreviated GTA, is a video game franchise created by game programmer David Jones and
    Mike Dailly, brothers Dan and Sam Houser, and game designer Zachary Clarke. It was originally developed in the UK by
    Rockstar North (formerly DMA Design) and published by Rockstar Games. The name of the series comes from the term
    "grand theft auto", which means grand auto heist.
</p>

<p class="tm-color-primary">About Grand Theft Auto V</p>

<p>When a young street hustler, a retired bank robber and a terrifying psychopath find themselves entangled with some of
    the most frightening and deranged elements of the criminal underworld, the U.S. government and the entertainment
    industry, they must pull off a series of dangerous heists to survive in a ruthless city in which they can trust
    nobody, least of all each other.</p>

<p>Grand Theft Auto V for PC offers players the option to explore the award-winning world of Los Santos and Blaine
    County in resolutions of up to 4k and beyond, as well as the chance to experience the game running at 60 frames per
    second.</p>

<p>The game offers players a huge range of PC-specific customization options, including over 25 separate configurable
    settings for texture quality, shaders, tessellation, anti-aliasing and more, as well as support and extensive
    customization for mouse and keyboard controls. Additional options include a population density slider to control car
    and pedestrian traffic, as well as dual and triple monitor support, 3D compatibility, and plug-and-play controller
    support.</p>

<p>Grand Theft Auto V for PC also includes Grand Theft Auto Online, with support for 30 players and two spectators.
    Grand Theft Auto Online for PC will include all existing gameplay upgrades and Rockstar-created content released
    since the launch of Grand Theft Auto Online, including Heists and Adversary modes.</p>

<p>The PC version of Grand Theft Auto V and Grand Theft Auto Online features First Person Mode, giving players the
    chance to explore the incredibly detailed world of Los Santos and Blaine County in an entirely new way.</p>

<p>Grand Theft Auto V for PC also brings the debut of the Rockstar Editor, a powerful suite of creative tools to quickly
    and easily capture, edit and share game footage from within Grand Theft Auto V and Grand Theft Auto Online. The
    Rockstar Editor’s Director Mode allows players the ability to stage their own scenes using prominent story
    characters, pedestrians, and even animals to bring their vision to life. Along with advanced camera manipulation and
    editing effects including fast and slow motion, and an array of camera filters, players can add their own music
    using songs from GTAV radio stations, or dynamically control the intensity of the game’s score. Completed videos can
    be uploaded directly from the Rockstar Editor to YouTube and the Rockstar Games Social Club for easy sharing.</p>

<p>Soundtrack artists The Alchemist and Oh No return as hosts of the new radio station, The Lab FM. The station features
    new and exclusive music from the production duo based on and inspired by the game’s original soundtrack.
    Collaborating guest artists include Earl Sweatshirt, Freddie Gibbs, Little Dragon, Killer Mike, Sam Herring from
    Future Islands, and more. Players can also discover Los Santos and Blaine County while enjoying their own music
    through Self Radio, a new radio station that will host player-created custom soundtracks.</p>