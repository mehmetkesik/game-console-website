<p class="tm-color-primary">About Spider-Man: Miles Morales</p>

<p>
    Be perfect. Be yourself.
    <br/><br/>
    The rise of our hero, Miles Morales, who gained extraordinary and explosive powers on his way to becoming
    Spider-Man experience.
</p>

<p>
    In Marvel's latest adventure in the Spider-Man universe, young Miles Morales tries to keep up with his new home
    while following in the footsteps of his master, Peter Parker, to become the new Spider-Man.
</p>

<p>
    But when a violent force threatens to destroy his new home, our eager hero realizes that with great power comes
    great responsibility. To save Marvel's entire New York City, Miles must claim the role of Spider-Man..
</p>

